export const getProvinceId = (provinceName) => {
  const province = nepalLocationData.provinces.find(
    (p) => p.name === provinceName
  );
  return province ? province.id : null;
};

export const getDistrictId = (districtName) => {
  for (const provinceId in nepalLocationData.districts) {
    const district = nepalLocationData.districts[provinceId].find(
      (d) => d.name === districtName
    );
    if (district) return district.id;
  }
  return null;
};

export const getAvailableDistricts = (provinceName) => {
  const provinceId = getProvinceId(provinceName);
  return provinceId ? nepalLocationData.districts[provinceId] || [] : [];
};

export const getAvailableMunicipalities = (districtName) => {
  const districtId = getDistrictId(districtName);
  return districtId
    ? nepalLocationData.municipalities[districtId] ||
        nepalLocationData.municipalities.default
    : [];
};
